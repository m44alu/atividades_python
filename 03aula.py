#variaveis em python
#nao ha necessidade de declara-las no inicio do programa
# basta criar no meio do algoritmo com a atribuiçao

teste = "maria luiza"
print(teste) # imprime a string "maria luiza"
print(type(teste)) # imprime o tipo da variavel, string
teste = 10
print(teste) # imprime o numero 10
print(type(teste)) # imprime o tipo da variavel, que é int
teste = 1.56
print(teste)#imprime o numero 1.74
print(type(teste))#imprime o tipo da variavel, que é float
teste = True
print(teste) # imprime o valor true
print(type(teste)) # imprime o tipo da variavel, que é bool